package U_9_OOP_Composition_Encapsulation_Polymorphism.U_9Composition;

public class Resulation {
    private String en;
    private String boy;

    public Resulation(String en, String boy) {
        this.en = en;
        this.boy = boy;
    }

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getBoy() {
        return boy;
    }

    public void setBoy(String boy) {
        this.boy = boy;
    }
}
